# =============================================================================
# scan_request.py — Vision Scan 요청 스키마 명세(주석 전용, 코드 없음)
# =============================================================================
#
# A. 공통 규약
#
# [좌표계]
# - 정규화 좌표 사용: 모든 x, y, w, h 및 폴리곤 점들은 0~1 범위
# - 기준 프레임: 업로드된 원본 이미지의 가로×세로
# - x, y는 좌상단 기준, w, h는 폭/높이 비율
# - 다각형은 [[x1, y1], [x2, y2], ...] 시계방향 권장
#
# [단위/제약]
# - 업로드 파일: JPEG 또는 PNG, 크기 ≤ MAX_IMAGE_BYTES
# - 한 요청당 이미지 1개
# - 문자열 인코딩: UTF-8
#
# B. 요청 스키마(명세)
#
# 엔드포인트: POST /api/v1/vision/scan
# 컨텐트 타입: multipart/form-data
#
# 필드 정의
# | 필드                   | 타입            | 필수  | 설명                                   |
# | -------------------- | ------------- | --- | ------------------------------------ |
# | image               | file          | 예   | 프레임 이미지 파일. JPEG/PNG만 허용.          |
# | guide_box           | object        | 아니오 | 가이드 박스 정규화 사각형. 없으면 전체 프레임 사용. |
# | guide_box.x         | number[0..1]  | 조건  | 좌상단 x                               |
# | guide_box.y         | number[0..1]  | 조건  | 좌상단 y                               |
# | guide_box.w         | number[0..1]  | 조건  | 폭                                     |
# | guide_box.h         | number[0..1]  | 조건  | 높이                                   |
# | user_query          | string        | 아니오 | 사용자 입력 키워드(브랜드/제품 일부). 매칭 가중치 반영 |
# | device_orientation  | enum          | 아니오 | portrait 또는 landscape. 회전 보정 힌트 |
# | request_id          | string        | 아니오 | 클라이언트 생성 요청 ID(로그 상관키)           |
# | ts                  | int(ms)       | 아니오 | 클라이언트 캡처 시각(밀리초, 로깅용)            |
#
# 검증 규칙
# - 파일 MIME 검사 필수: image/jpeg, image/png만 허용
# - guide_box가 있으면 x,y,w,h 모두 0~1 범위, w,h > 0
# - user_query 길이 제한: 최대 80자
#
# =============================================================================
# 이 파일은 ‘명세’만 담습니다. 후속 단계에서 Pydantic 모델로 구현합니다.
# =============================================================================
